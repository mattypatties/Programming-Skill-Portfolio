i=0 #set starting number
while i<=50: #loop until it gets to 50
    print(i)
    i+=1 #add 1 to the value of i
    #now the i has a new value
print("")

j=50 #do the same thing but in descending order
while j>=0: 
    print(j)
    j-=1
print("")

